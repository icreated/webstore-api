package co.icreated.wstore.bean;

public class Shipper {

  int id = 0;
  String name;
  String description;

  public Shipper() {}


  public Shipper(String name) {
    super();
    this.name = name;
  }


  public Shipper(int id, String name, String description) {
    super();
    this.id = id;
    this.name = name;
    this.description = description;
  }

  public int getId() {
    return id;
  }

  public String getName() {
    return name;
  }

  public String getDescription() {
    return description;
  }

  public void setDescription(String description) {
    this.description = description;
  }

  public void setName(String name) {
    this.name = name;
  }



}
